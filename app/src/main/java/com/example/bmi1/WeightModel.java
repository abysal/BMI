package com.example.bmi1;

public class WeightModel {
    private Float weight1, height1;

    public WeightModel(Float weight, Float height) {
        this.weight1 = weight;
        this.height1 = height;
    }

    public Float getWeight1() {
        return weight1;
    }

    public void setWeight1(Float weight1) {
        this.weight1 = weight1;
    }

    public Float getHeight1() {
        return height1;
    }

    public void setHeight1(Float height1) {
        this.height1 = height1;
    }
}
